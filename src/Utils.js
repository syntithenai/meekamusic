Utils.snakeToCamel = function(s){
    if (s) {
     var c = s.replace(/(_\w)/g, function(m){return ' ' + m[1].toUpperCase() ;});
    c = c.charAt(0).toUpperCase() + c.slice(1)
    return c   
    }
    return '';
}

