Utils.setFullScreen = function(value,pushElement) {
        var element = document.body;

        if (pushElement instanceof HTMLElement) {
            console.log(['SSET ELEMENT',pushElement]);
            element = pushElement;
        }

       // var isFullscreen = document.webkitIsFullScreen || document.mozFullScreen || false;

        element.requestFullScreen = element.requestFullScreen || element.webkitRequestFullScreen || element.mozRequestFullScreen || function () { return false; };
        document.cancelFullScreen = document.cancelFullScreen || document.webkitCancelFullScreen || document.mozCancelFullScreen || function () { return false; };

        //isFullscreen ? document.cancelFullScreen() : element.requestFullScreen();
        if (value) {
            element.requestFullScreen();
        } else {
            document.cancelFullScreen()
        }
    };    
    
Utils.getSearchLink = function(track) {
    let link='';
    let base = '';
   // console.log(['GETSEARCHLINK',track.url,track]);
    if (track) {
        let artistName = track.artist ? track.artist : '';
        let albumName = track.album ? track.album : '';
        
        if (track.url) {
            if (track.url.indexOf('youtube.com') !== -1 ) {
                base="/meeka/youtube/";
                artistName = track.title;
                albumName = '';
            } else if (track.url.indexOf('jamendo.com') !== -1) {
                base="/meeka/jamendo/";
            } else if (track.url.indexOf('freemusicarchive.org') !== -1) {
                base="/meeka/fma/";
            } else if (track.path && track.path.length > 0) {
                base="/meeka/search/";
            } else {
                base="/meeka/search/";
            }
        }
        if (albumName.length > 0) {
            link = base+encodeURI(artistName + ' ' + albumName);
        } else {
            link = base+encodeURI(artistName);
        }
    }
  
    return link;
}
